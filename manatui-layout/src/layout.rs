/// defines the sizing strategy for an element.
#[derive(Default, Clone, Copy, Debug)]
pub enum Size {
    /// the element will be exactly `x` cells in size, clamped to it's parent's size.
    Fixed(u16),
    #[default]
    /// the element will grow to fit it's children, up to it's own parent's size
    Fit,
    /// the element will grow to fill it's parent, following a water-filling strategy, i.e. the smallest elements grow first.
    Grow,
    /// the element will be a percentage of it's parent size.
    Percentage(u8),
}

#[bon::bon]
impl Size {
    const fn compute_from_parent(self, parent_size: &mut f32) -> Option<u16> {
        match self {
            Size::Fixed(f) => Some(f),
            Size::Fit => None,
            Size::Grow => None,
            Size::Percentage(p) => {
                let size = (p as f64) * *parent_size as f64 / 100.0;
                match size.fract() {
                    0.0..0.5 => {
                        let size = size.round();
                        Some(size as u16)
                    }
                    fract @ 0.5.. => {
                        let size = size.round();
                        *parent_size -= fract as f32;
                        Some(size as u16)
                    }
                    _ => unreachable!(),
                }
            }
        }
    }
    #[builder]
    const fn apply(self, to: u16, parent_size: Option<u16>, space_used: Option<u16>) -> u16 {
        match (self, parent_size, space_used) {
            (Size::Fixed(f), _, _) => f,
            (Size::Percentage(p), Some(container), _) => {
                let size = (p as f64) * container as f64 / 100.0;
                let size = size.round();
                size as u16
            }
            (Size::Fit | Size::Grow, _, Some(space_used)) => space_used,
            _ => to,
        }
    }
}

/// defines the alignment strategy on the main axis.
#[derive(Default, Clone, Copy, Debug, strum::EnumIter)]
pub enum MainJustify {
    /// aligns the items toward the start of the container.
    ///
    /// ```plaintext
    /// ╭Start─────────────────╮
    /// │╭──╮╭──╮╭──╮          │
    /// ││#0││#1││#2│          │
    /// │╰──╯╰──╯╰──╯          │
    /// ╰──────────────────────╯
    /// ```
    #[default]
    Start,

    /// centers the items.
    ///
    /// ```plaintext
    /// ╭Center────────────────╮
    /// │     ╭──╮╭──╮╭──╮     │
    /// │     │#0││#1││#2│     │
    /// │     ╰──╯╰──╯╰──╯     │
    /// ╰──────────────────────╯
    /// ```
    Center,

    /// distributes the remaining space evenly between elements. inserts as much space between the elments as possible
    ///
    /// ```plaintext
    /// ╭SpaceBetween──────────╮
    /// │╭──╮     ╭──╮     ╭──╮│
    /// ││#0│     │#1│     │#2││
    /// │╰──╯     ╰──╯     ╰──╯│
    /// ╰──────────────────────╯
    /// ```
    SpaceBetween,

    /// distributes the remaining space evenly between elements such that there is an equal amount of space before
    /// and after each item.
    ///
    /// NOTE: this mode usually has poor results on small containers due to rounding issues.
    ///
    /// ```plaintext
    /// ╭SpaceAround───────────╮
    /// │ ╭──╮   ╭──╮   ╭──╮   │
    /// │ │#0│   │#1│   │#2│   │
    /// │ ╰──╯   ╰──╯   ╰──╯   │
    /// ╰──────────────────────╯
    /// ```
    SpaceAround,

    /// distributes the remaining space evenly between elements such that there is an equal amount of on each side of each item,
    /// accounting for the doubling of space that happens with [`Justify::SpaceAround`]
    ///
    /// NOTE: this mode usually has poor results on small containers due to rounding issues.
    ///
    /// ```plaintext
    /// ╭SpaceEvenly───────────╮
    /// │  ╭──╮  ╭──╮  ╭──╮    │
    /// │  │#0│  │#1│  │#2│    │
    /// │  ╰──╯  ╰──╯  ╰──╯    │
    /// ╰──────────────────────╯
    /// ```
    SpaceEvenly,

    /// aligns the items toward the end of the container.
    ///
    /// ```plaintext
    /// ╭End───────────────────╮
    /// │          ╭──╮╭──╮╭──╮│
    /// │          │#0││#1││#2││
    /// │          ╰──╯╰──╯╰──╯│
    /// ╰──────────────────────╯
    /// ```
    End,
}

#[derive(Default, Clone, Copy, Debug, strum::EnumIter)]
/// defines the alignment strategy on the cross axis.
pub enum CrossJustify {
    /// aligns the items toward the start of the container.
    #[default]
    Start,
    /// centers the items.
    Center,
    /// aligns the items toward the end of the container.
    End,
}

pub(crate) trait ManaComponent {
    fn run_postprocess(ctx: &mut ElementCtx, commands: &mut CommandBuffer);
}

/// equivalent to a `(MainJustify::Center, CrossJustify::Center)` bundle. this will make a container
/// center its children.
pub struct Center;

impl ManaComponent for Center {
    fn run_postprocess(ctx: &mut ElementCtx, _: &mut CommandBuffer) {
        // do not use the shared buffer
        let mut commands = CommandBuffer::new();
        for (node, _) in ctx.query_mut::<(Entity, &Center)>() {
            commands.insert_one(node, MainJustify::Center);
            commands.insert_one(node, CrossJustify::Center);
        }
        commands.run_on(ctx);
    }
}

impl Size {
    fn should_clamp(&self) -> bool {
        match self {
            Size::Fixed(_) => false,
            Size::Fit => false,
            Size::Grow => false,
            Size::Percentage(_) => false,
        }
    }
    fn is_grow(&self) -> bool {
        matches!(self, Size::Grow)
    }

    /// Returns `true` if the size is [`Percentage`].
    ///
    /// [`Percentage`]: Size::Percentage
    #[must_use]
    pub fn is_percentage(&self) -> bool {
        matches!(self, Self::Percentage(..))
    }

    /// Returns percentage value as `Some(u8)` if the size is [`Percentage`].
    ///
    /// [`Percentage`]: Size::Percentage
    #[must_use]
    pub fn as_percentage(&self) -> Option<&u8> {
        if let Self::Percentage(v) = self {
            Some(v)
        } else {
            None
        }
    }

    pub(crate) fn as_fixed(&self) -> Option<&u16> {
        if let Self::Fixed(v) = self {
            Some(v)
        } else {
            None
        }
    }
}

trait AxisExt {
    type El;
    fn main_axis(&self, dir: Direction) -> &Self::El;
    fn cross_axis(&self, dir: Direction) -> &Self::El;
}

trait AxisExtMut: AxisExt {
    fn main_axis_mut(&mut self, dir: Direction) -> &mut Self::El;
    fn cross_axis_mut(&mut self, dir: Direction) -> &mut Self::El;
}
macro_rules! impl_axis_ext {
    ($($type:ty => $el:ty),* $(,)?) => {
        $(
            impl AxisExt for $type {
                type El = $el;

                #[inline(always)]
                fn main_axis(&self, dir: Direction) -> &Self::El {
                    match dir {
                        Direction::Horizontal => &self.x,
                        Direction::Vertical => &self.y,
                    }
                }

                #[inline(always)]
                fn cross_axis(&self, dir: Direction) -> &Self::El {
                    match dir {
                        Direction::Horizontal => &self.y,
                        Direction::Vertical => &self.x,
                    }
                }
            }

            impl AxisExtMut for $type {
                #[inline(always)]
                fn main_axis_mut(&mut self, dir: Direction) -> &mut Self::El {
                    match dir {
                        Direction::Horizontal => &mut self.x,
                        Direction::Vertical => &mut self.y,
                    }
                }

                #[inline(always)]
                fn cross_axis_mut(&mut self, dir: Direction) -> &mut Self::El {
                    match dir {
                        Direction::Horizontal => &mut self.y,
                        Direction::Vertical => &mut self.x,
                    }
                }
            }
        )*
    };
}

impl_axis_ext!(
    U16Vec2 => u16,
    Vec2 => f32,
);

impl<T, U, W> AxisExt for (U, W)
where
    U: Deref<Target = T>,
    W: Deref<Target = T>,
{
    type El = T;

    fn main_axis(&self, dir: Direction) -> &Self::El {
        match dir {
            Direction::Horizontal => &self.0,
            Direction::Vertical => &self.1,
        }
    }

    fn cross_axis(&self, dir: Direction) -> &Self::El {
        match dir {
            Direction::Horizontal => &self.1,
            Direction::Vertical => &self.0,
        }
    }
}

impl<T, U, W> AxisExtMut for (U, W)
where
    U: DerefMut<Target = T>,
    W: DerefMut<Target = T>,
{
    fn main_axis_mut(&mut self, dir: Direction) -> &mut Self::El {
        match dir {
            Direction::Horizontal => self.0.deref_mut(),
            Direction::Vertical => self.1.deref_mut(),
        }
    }

    fn cross_axis_mut(&mut self, dir: Direction) -> &mut Self::El {
        match dir {
            Direction::Horizontal => &mut self.1,
            Direction::Vertical => &mut self.0,
        }
    }
}
